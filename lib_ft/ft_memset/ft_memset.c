#include <stdlib.h>

void *ft_memset(void *s, int c, size_t n)
{
    int i;
    char *s1 = (char *) s;

    i = 0;
    while (n > 0)
    {
        s1[i] = c;
        i++;
        n--;
    }
    return (s1);
}

